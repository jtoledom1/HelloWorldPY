# Solicita al usuario que ingrese el primer número
num1 = input("Ingrese el primer número: ")

# Solicita al usuario que ingrese el segundo número
num2 = input("Ingrese el segundo número: ")

# Convierte las entradas a números (float en este caso, para manejar decimales)
num1 = float(num1)
num2 = float(num2)

# Realiza la suma de los dos números
suma = num1 + num2

# Imprime el resultado
print(f"La suma de {num1} y {num2} es {suma}")
